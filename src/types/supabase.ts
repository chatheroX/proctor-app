export type FlaggedEventType =
  | 'visibility_hidden'
  | 'visibility_visible'
  | 'fullscreen_entered'
  | 'fullscreen_exited'
  | 'blur'
  | 'focus'
  | 'copy_attempt'
  | 'paste_attempt';

export interface FlaggedEvent {
  type: FlaggedEventType;
  timestamp: Date; // Will be stored as ISO string in JSONB
  studentId: string;
  examId: string;
  details?: string;
}
